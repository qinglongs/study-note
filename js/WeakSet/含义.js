/**
 * WeakSet结构与Set类似，也不是重复的值的集合。但是，它与Set有两个区别
 ****WeakSet的成员只能是对象，而不是其他类型的值***
 * **WeakSet中的对象都是弱引用，即垃圾回收机制不考虑WeakSet对该对象的引用**
 * **也就是说，如果其他对象都不再引用该对象，那么垃圾回收机制会自动回收该对象所占的内存，不考虑该对象还存在于WeakSet之中**
 * **WeakSet不可遍历**
 */

 